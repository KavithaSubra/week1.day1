package week1.day1;

public class LearnOperators {
public static void main(String[] args) {
	System.out.println(20+10);
	System.out.println(20-10);
	System.out.println(20*10);
	System.out.println(20/10);
	System.out.println(22%10);
	System.out.println(20<10);
	System.out.println(20!=10);
	System.out.println(20==10 && 20>3);
	System.out.println(20!=10 || 20<3);
}
}
